-- AI-AgriBench Frontend - Minimal Supabase schema
-- Run this script in Supabase SQL Editor.

create table if not exists public.evaluations (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  judge_model text not null,
  judge_temperature numeric not null default 0,
  num_processes integer not null default 1,
  subject_model text not null
);

create table if not exists public.scores (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  evaluation_id bigint not null references public.evaluations(id) on delete cascade,
  question_id text not null,
  accuracy numeric not null,
  completeness numeric not null,
  conciseness numeric not null,
  relevance numeric not null,
  categories text[] not null default '{}'
);

create index if not exists idx_scores_evaluation_id on public.scores(evaluation_id);
create index if not exists idx_scores_categories on public.scores using gin(categories);

-- Read-only public access for the frontend leaderboard.
-- Keep insert/update/delete restricted unless you intentionally build an admin workflow.
alter table public.evaluations enable row level security;
alter table public.scores enable row level security;

drop policy if exists "Public read evaluations" on public.evaluations;
create policy "Public read evaluations"
  on public.evaluations
  for select
  using (true);

drop policy if exists "Public read scores" on public.scores;
create policy "Public read scores"
  on public.scores
  for select
  using (true);

